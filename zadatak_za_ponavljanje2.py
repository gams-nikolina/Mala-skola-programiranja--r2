#2. Zadatak za ponavljanje: Luka i Ivan su na plaži skupljali školjke. Luka je uspio skupiti 4 puta više školjki nego Ivan. Napravi program koji računa koliko je svaki od njih skupio školjaka te koliko su zajedno skupili školjaka.
n=int(input('Koliko školjaka je Ivan skupio? '))
print("Ivan je skupio", n, "školjke, a Luka ih je skupio",n*4,". Zajedno su ih skupili", (n*4)+n,"." )            
